# List of short text messages
messages = [
    "Hello, world Tristan is the greatest!",
    "This is a test message from Tristan.",
    "Python is fun!",
    "Learning to code is exciting.",
    "Have a great day!"
]

# Function to print each text message
def show_messages(message_list):
    for message in message_list:
        print(message)

show_messages(messages)